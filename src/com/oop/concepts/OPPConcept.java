	/*
		Object-Oriented Programming (OOP) is a programming paradigm based on the real world Object.
		Object which contain data and methods (functions) to manipulate the data. 
		Java is a fully object-oriented programming language, and understanding OOP concepts is crucial for writing efficient and maintainable Java code.
		Object means a real-world entity such as a pen, chair, table, computer, watch, etc.
		
			# Object
				Any entity that has state and behavior is known as an object. For example, a chair, pen, table, keyboard, bike, etc. It can be physical or logical.
				
			# Class
				* Collection of objects is called class. It is a logical entity.
				* A class can also be defined as a blueprint.
				
			# Inheritance
				* Acquires all the properties and behaviors of a parent object.
				* Reusability.
				* Used to achieve runtime polymorphism.
				
			# Polymorphism
				If one task is performed in different ways.
				
			# Abstraction
				Hiding internal implementation and showing functionality.
				Eg. Calculator
				
			# Encapsulation
				Binding (or wrapping) code and data together into a single unit.
		
		# Four Pillars Of Object-Oriented Programming (OOP)
			1. Encapsulation
			
			2. Inheritance
			
			3. Polymorphism
			
			4. Abstraction
		
	
	*/
